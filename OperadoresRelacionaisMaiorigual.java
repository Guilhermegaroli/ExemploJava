package exemplo;

public class OperadoresRelacionaisMaiorigual {

	public static void main(String[] args) {
		
		int x = 10;
		int y = 20;

		if (x >= y) {
           System.out.println("Numero x é maior ou igual ao numero y");
			
		} else {
           System.out.println("Numero x é menor que o numero y");

	}

}
}
